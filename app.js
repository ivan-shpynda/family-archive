const container = document.querySelector('.photos');

const renderPhotos = (data) => {
  let html = '';

  data.forEach(element => {
    const {id, year, place, description, src, alt} = element;

    const htmlSegment = `<div class="photo">
    <img
      src="${src}"
      alt="${alt}"
      class="photo__img"
      onclick="showPhoto('${src}')"
    >
    <div class="photo__text-block">
      <p class="photo__year"><span class="photo__property">ID: </span>${id}</p>
      <p class="photo__year"><span class="photo__property">Рік: </span>${year}</p>
      <p class="photo__place"><span class="photo__property">Місце: </span>${place}</p>
      <p class="photo__description"><span class="photo__property">Опис: </span>${description}</p>
    </div>
  </div>`;

  html += htmlSegment;
  });

  container.innerHTML = html;
};

renderPhotos(data);


const modal = document.getElementById('myModal');
const modalImage = document.getElementById('img01');
const caption = document.getElementById('caption');
const closeModal = document.querySelector('.close');

const showPhoto = (photo) => {
  modal.style.display = 'block';
  modalImage.src = photo;
  modal.classList.add("show-up");
}

window.addEventListener('click', function(evt) {
  if (evt.target === modal) {
    modal.style.display = 'none';
  }
});

closeModal.addEventListener('click', function(evt) {
  modal.style.display = 'none';
});

window.addEventListener('keydown', function(evt) {
  if(evt.key === 'Escape') {
    modal.style.display = 'none';
  }
})
